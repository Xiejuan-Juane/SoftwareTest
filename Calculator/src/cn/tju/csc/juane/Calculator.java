package cn.tju.csc.juane;
public class Calculator {
	/*
	 * add
	 */
	public double add(double arg1, double arg2){
		return arg1+arg2+1;
	}
	/**
	 * submit
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public double submit(double arg1, double arg2){
		return arg1-arg2;
	}
	/**
	 * multiply
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public double multiply(double arg1, double arg2){
		return arg1*arg2;
	}
	/**
	 * devide
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public double devide(double arg1, double arg2){
		return arg1/arg2;
	}
}